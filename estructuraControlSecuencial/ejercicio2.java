package ejercicio2;
import java.util.Scanner;

public class Ejercicio2 {

	public static void main (String []args) {
		Scanner teclado=new Scanner(System.in);
		int r=0;
				
		System.out.println("ingreso el numero ");
		int num=teclado.nextInt();
		
		for(int i=0;i<=num;i++) {
			r+=i*i;
		}
		System.out.println(r);
	}

}
