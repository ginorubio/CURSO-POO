package ejercicio4;
import java.util.Scanner;

public class Ejercicio4 {

	public static void main (String []args){
		Scanner teclado=new Scanner(System.in);
		System.out.print("Ingrese la primera coordenada x: ");
		int x1=teclado.nextInt();
		System.out.print("Ingrese la primera coordenada y: ");
		int y1=teclado.nextInt();
		
		System.out.print("Ingrese la segunda coordenada x: ");
		int x2=teclado.nextInt();
		System.out.print("Ingrese la segunda coordenada y: ");
		int y2=teclado.nextInt();
		
		int distancia=(int)Math.sqrt(Math.pow(x2-x1,2)+Math.pow(y2-y1,2));
		
		System.out.println("La distancia entre los dos puntos es: "+distancia);
	}
}
